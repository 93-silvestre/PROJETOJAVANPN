package br.com.generation.projetojava2;

public class Categorias {
	
	private String Alimentos;
	private String Roupas;
	private String Brinquedos;
	private String Farmacia;
	private String Outros;
	
	
	public String getAlimentos() {
		return Alimentos;
	}
	public void setAlimentos(String alimentos) {
		Alimentos = alimentos;
	}
	public String getRoupas() {
		return Roupas;
	}
	public void setRoupas(String roupas) {
		Roupas = roupas;
	}
	public String getBrinquedos() {
		return Brinquedos;
	}
	public void setBrinquedos(String brinquedos) {
		Brinquedos = brinquedos;
	}
	public String getFarmacia() {
		return Farmacia;
	}
	public void setFarmacia(String farmacia) {
		Farmacia = farmacia;
	}
	public String getOutros() {
		return Outros;
	}
	public void setOutros(String outros) {
		Outros = outros;
	}
	
	

	
}